func ExampleIsValid() {
	// ICCID
	fmt.Printf("%s → %t\n", "8970199240502489916", IsValid("8970199240502489916"))

	// Credit card
	fmt.Printf("%s → %t\n", "4513349830885029", IsValid("4513349830885029"))

	// Invalid
	fmt.Printf("%s → %t\n", "12341234123412345", IsValid("12341234123412345"))

	// Output:
	// 8970199240502489916 → true
	// 4513349830885029 → true
	// 12341234123412345 → false
}

func ExampleCalculate() {
	checksum, _ := Calculate("8970199240502489916")
	fmt.Printf("%-19s → %s\n", "8970199240502489916", checksum)

	checksum, _ = Calculate("897019924050248991")
	fmt.Printf("%-19s → %s\n", "897019924050248991", checksum)

	// Output:
	// 8970199240502489916 → 6
	// 897019924050248991  → 6
}

func ExampleNormalize() {
	v, _ := Normalize("8970199240502489916")
	fmt.Printf("%-19s → %s\n", "8970199240502489916", v)

	v, _ = Normalize("897019924050248991")
	fmt.Printf("%-19s → %s\n", "897019924050248991", v)

	// Output:
	// 8970199240502489916 → 8970199240502489916
	// 897019924050248991  → 8970199240502489916
}
